// Data types - Kiểu dữ liệu

// 1, Các kiểu dữ liệu thường dùng
// 2, Cách khai báo biến và gán giá trị
// 3, Cách console log, comment
// 4, let và const

// Number
let a = 1
let b = 1.2

// String
let strA = 'Hello'
let strB = "Xin chao"

// Boolean
let bolA = true
let bolB = false

// undefined, null
let a
let b = null

// Operators - Toán tử 

// 1, Arithmetic operators - Toán tử cho số 
let a = 1 + 1 // 2
let b = a - 1 // 1
let c = a * 2 // 4
let d = 2 / 2 // 1
let e = 3 % 2 // 1

// 2, Comparison operators - Toán tử so sánh 
let a = 3 > 1 // true
let b = 3 == '3' // true
let c = 3 === '3' // false
let d = 3 !== 2 // true
let e = 2 >= 2 // true
let f = 3 <= 2 // false

// 3, Logical operators - Toán tử logic
// AND
let a = true && true // true
let b = true && false // false

// OR
let c = true || false // true

// NOT
let d = !true // false

// 4, String operators - Toán tử cho string
let strA = 'Xin chao' + ' ' + 'Khanh' // Xin chao Khanh
